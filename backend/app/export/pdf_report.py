"""
PDF Report Generator

Generates a comprehensive PDF report of the synthesis results.
Two modes:
  - 'full': Educational report with theory, step-by-step derivations,
    equations, diagrams, and complete results
  - 'concise': Summary with key parameters, error metrics, and mechanism diagram

Uses ReportLab for PDF generation.
"""

import io
import math
from typing import Dict, List, Optional

from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm, cm
from reportlab.lib.enums import TA_CENTER, TA_LEFT, TA_RIGHT
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
    PageBreak, HRFlowable, Image,
)
from reportlab.graphics.shapes import Drawing, Line, Circle, String, Rect
from reportlab.graphics import renderPDF


def generate_pdf_bytes(data: Dict, mode: str = "full") -> bytes:
    """
    Generate PDF report as bytes.

    Args:
        data: export data dict (mechanism, errorMetrics, couplerCurve, etc.)
        mode: 'full' or 'concise'

    Returns:
        PDF file content as bytes
    """
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer, pagesize=A4,
        leftMargin=20 * mm, rightMargin=20 * mm,
        topMargin=25 * mm, bottomMargin=20 * mm,
    )

    styles = _build_styles()
    story = []

    # Title page
    story.extend(_build_title_page(styles, data))
    story.append(PageBreak())

    if mode == "full":
        # Full educational report
        story.extend(_build_theory_section(styles))
        story.append(PageBreak())
        story.extend(_build_type_synthesis_section(styles, data))
        story.extend(_build_number_synthesis_section(styles, data))
        story.append(PageBreak())

    # Results section (both modes)
    story.extend(_build_results_section(styles, data))
    story.extend(_build_error_section(styles, data))
    story.extend(_build_performance_section(styles, data))

    if mode == "full":
        story.extend(_build_equations_section(styles))

    # Footer info
    story.extend(_build_footer(styles))

    doc.build(story)
    return buffer.getvalue()


