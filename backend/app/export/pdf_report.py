def _build_performance_section(styles, data) -> list:
    """Build performance checks section."""
    story = []
    mech = data.get("mechanism", {})

    story.append(Paragraph("Performance & Feasibility", styles["h1"]))

    checks = []
    grashof = mech.get("grashofType") or data.get("grashofType")
    if grashof:
        status = "✓ PASS" if grashof != "non-grashof" else "✗ FAIL"
        checks.append(["Grashof Condition", grashof.replace("-", " ").title(), status])

    trans = data.get("transmissionAngle") or mech.get("transmissionAngle")
    if trans and isinstance(trans, dict):
        min_a = trans.get("min", 0)
        max_a = trans.get("max", 0)
        status = "✓ PASS" if min_a >= 30 else "✗ FAIL"
        checks.append(["Transmission Angle", f"Min: {min_a:.1f}° / Max: {max_a:.1f}°", status])

    margin = mech.get("grashofMargin")
    if margin is not None:
        checks.append(["Grashof Margin", f"{margin:.2f}", ""])

    if checks:
        t = Table(
            [["Check", "Value", "Status"]] + checks,
            colWidths=[50 * mm, 55 * mm, 25 * mm],
        )
        t.setStyle(TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#3170e3")),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTSIZE", (0, 0), (-1, -1), 9),
            ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
            ("TOPPADDING", (0, 0), (-1, -1), 4),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
        ]))
        story.append(t)

    return story


def _build_equations_section(styles) -> list:
    """Build equations appendix (full mode only)."""
    story = []
    story.append(PageBreak())
    story.append(Paragraph("Appendix: Key Equations", styles["h1"]))

    equations = [
        ("Gruebler's Equation", "F = 3(n - 1) - 2j₁ - j₂"),
        ("Loop-Closure (vector)", "r₂ + r₃ = r₁ + r₄"),
        ("Freudenstein's Equation", "R₁·cos(θ₂) - R₂·cos(θ₄) + R₃ = cos(θ₂ - θ₄)"),
        ("Grashof Condition", "s + l ≤ p + q"),
        ("Transmission Angle", "cos(μ) = (a₃² + a₄² - a₁² - a₂² + 2a₁a₂cos(θ₂)) / (2a₃a₄)"),
        ("Chebyshev Spacing", "x_k = (a+b)/2 + (b-a)/2 · cos((2k-1)π / 2n)"),
        ("Closest-Point Error", "f(x) = (1/N) Σ min_j [(Px_j - Dx_k)² + (Py_j - Dy_k)²]"),
    ]

    for name, eq in equations:
        story.append(Paragraph(f"<b>{name}:</b>", styles["body"]))
        story.append(Paragraph(eq, styles["equation"]))

    return story


