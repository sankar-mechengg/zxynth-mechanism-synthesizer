def _build_theory_section(styles) -> list:
    """Build the theory/education section (full mode only)."""
    story = []
    story.append(Paragraph("1. Mechanism Synthesis Theory", styles["h1"]))

    story.append(Paragraph("1.1 The Three Classical Problems", styles["h2"]))
    story.append(Paragraph(
        "Mechanism synthesis addresses three fundamental problems: "
        "<b>Path Generation</b> (coupler point traces a prescribed curve), "
        "<b>Function Generation</b> (output angle is a function of input angle), and "
        "<b>Motion Generation</b> (coupler link passes through prescribed positions and orientations).",
        styles["body"],
    ))

    story.append(Paragraph("1.2 Degrees of Freedom", styles["h2"]))
    story.append(Paragraph(
        "The mobility of a planar mechanism is computed using Gruebler's equation:",
        styles["body"],
    ))
    story.append(Paragraph("F = 3(n - 1) - 2j₁ - j₂", styles["equation"]))
    story.append(Paragraph(
        "where n = total links, j₁ = lower pairs (revolute + prismatic), j₂ = higher pairs. "
        "For single-input mechanisms, we require F = 1.",
        styles["body"],
    ))

    story.append(Paragraph("1.3 Synthesis Stages", styles["h2"]))
    story.append(Paragraph(
        "<b>Type Synthesis:</b> Select mechanism family (linkage, cam, gear). "
        "<b>Number Synthesis:</b> Determine link/joint count, verify DOF. "
        "<b>Dimensional Synthesis:</b> Compute exact link lengths and pivot locations "
        "using analytical methods (Freudenstein, Burmester) or global optimization (DE, GA, PSO, SA).",
        styles["body"],
    ))

    return story


