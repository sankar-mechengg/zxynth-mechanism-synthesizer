def _build_number_synthesis_section(styles, data) -> list:
    """Build number synthesis section with Gruebler calculation."""
    story = []
    story.append(Paragraph("3. Number Synthesis", styles["h1"]))

    mech = data.get("mechanism", {})
    mech_type = mech.get("type", "four_bar")

    configs = {
        "four_bar": (4, 4, 0),
        "six_bar_watt": (6, 7, 0),
        "six_bar_stephenson": (6, 7, 0),
        "slider_crank": (4, 4, 0),
    }
    n, j1, j2 = configs.get(mech_type, (4, 4, 0))
    dof = 3 * (n - 1) - 2 * j1 - j2

    story.append(Paragraph("Gruebler's Equation Verification:", styles["body"]))
    story.append(Paragraph(f"F = 3({n} - 1) - 2({j1}) - {j2} = {dof}", styles["equation"]))
    story.append(Paragraph(
        f"DOF = {dof} {'✓ (single-input mechanism)' if dof == 1 else '✗ (invalid)'}",
        styles["body"],
    ))
    return story


def _build_results_section(styles, data) -> list:
    """Build the mechanism parameters results section."""
    story = []
    story.append(Paragraph("Mechanism Parameters", styles["h1"]))

    mech = data.get("mechanism", {})
    if not mech:
        story.append(Paragraph("No mechanism data available.", styles["body"]))
        return story

    # Parameters table
    params = []
    param_map = [
        ("a₁ (ground)", "a1"), ("a₂ (crank)", "a2"),
        ("a₃ (coupler)", "a3"), ("a₄ (rocker)", "a4"),
        ("p (coupler pt distance)", "p"), ("α (coupler pt angle)", "alpha"),
    ]

    for label, key in param_map:
        val = mech.get(key)
        if val is not None:
            unit = "°" if "angle" in label.lower() or "α" in label else ""
            params.append([label, f"{val:.4f}{unit}"])

    if mech.get("pivotA"):
        pa = mech["pivotA"]
        params.append(["Pivot A", f"({pa[0]:.2f}, {pa[1]:.2f})"])
    if mech.get("pivotD"):
        pd = mech["pivotD"]
        params.append(["Pivot D", f"({pd[0]:.2f}, {pd[1]:.2f})"])

    if params:
        t = Table(
            [["Parameter", "Value"]] + params,
            colWidths=[70 * mm, 60 * mm],
        )
        t.setStyle(TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#1e4da8")),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTSIZE", (0, 0), (-1, -1), 9),
            ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
            ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, colors.HexColor("#f8fafc")]),
            ("TOPPADDING", (0, 0), (-1, -1), 4),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
            ("FONTNAME", (1, 1), (1, -1), "Courier"),
        ]))
        story.append(t)
        story.append(Spacer(1, 5 * mm))

    return story


